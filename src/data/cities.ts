// 找出离 [lng, lat] 最近的城市（用平面近似，足够这种 UI 标签用）
export function nearestCity(lng: number, lat: number): { name: string; distance: number } {
  let best = CITIES[0];
  let bestD = Infinity;
  for (const c of CITIES) {
    // 经纬度差，按纬度修正
    const dx = (c[1] - lng) * Math.cos((lat * Math.PI) / 180);
    const dy = c[2] - lat;
    const d = dx * dx + dy * dy;
    if (d < bestD) {
      bestD = d;
      best = c;
    }
  }
  // 转换成大致公里数
  const km = Math.sqrt(bestD) * 111;
  return { name: best[0], distance: km };
}
